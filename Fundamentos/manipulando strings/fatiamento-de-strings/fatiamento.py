nome = 'Eric Ranzani de França'

print(nome[0])

# vai imprimir os caracteres ate o 8
print(nome[:9])

# pegar o ultimo indice
print(nome[-1])

# vai imprimir apartir do caracter 10 adiante
print(nome[10:])

# vai imprimir caracteres do indice 10 ate 16
print(nome[10:16])

# vai imprimir caracteres do indice 10 ate 16
# com adição do step, ou seja, ele vai de 2 em 2
print(nome[10:16:2])

# traz a copia
print(nome[:])

# faz o espelhamento da variavel
print(nome[::-1])

